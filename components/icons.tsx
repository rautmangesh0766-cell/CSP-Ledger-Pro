export const TransactionTypeIcon: React.FC<{ type: TransactionType }> = ({ type }) => {
    switch (type) {
        case TxTypeEnum.CUSTOMER_DEPOSIT:
            return <ArrowUpIcon className="w-5 h-5 text-green-600" />;
        case TxTypeEnum.CUSTOMER_WITHDRAWAL:
            return <ArrowDownIcon className="w-5 h-5 text-red-600" />;
        case TxTypeEnum.CUSTOMER_TRANSFER:
            return <ArrowDownIcon className="w-5 h-5 text-purple-600" />;
        case TxTypeEnum.CSP_DEPOSIT_TO_BANK:
            return <BankIcon className="w-5 h-5 text-blue-600" />;
        case TxTypeEnum.CSP_WITHDRAWAL_FROM_BANK:
            return <CashIcon className="w-5 h-5 text-yellow-600" />;
        default:
            return null;
    }
};